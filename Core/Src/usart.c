void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
  if (huart->Instance == USART1) {

    if(sync_state != DMA_STATE) {
      switch (sync_state) {
        case SYNC_BYTE_1_STATE:
          if (sync_buf[0] == 0xAA) {
            sync_state = SYNC_BYTE_2_STATE;
          } 
          break;

        case SYNC_BYTE_2_STATE:
          if (sync_buf[0] == 0xBB) {
            sync_state = SYNC_BYTE_3_STATE;
          } else {
            sync_state = SYNC_BYTE_1_STATE;
          }
          break;

        case SYNC_BYTE_3_STATE:
          if (sync_buf[0] == 0xCC) {
            sync_state = LENGTH_STATE;
          } else {
            sync_state = SYNC_BYTE_1_STATE;
          }
          break;

        case LENGTH_STATE:
          // For short headers, this is the message length, not including header or CRC
          // For long headers, this is the header length
          // TODO: Add support for long headers without manual length defs
          message_length = sync_buf[0];
          sync_state = MESSAGE_ID_STATE;
          break;

        case MESSAGE_ID_STATE:
          message_id = (sync_buf[1] << 8) | sync_buf[0]; // (big endian)

          // message id will be first two bytes of message_buf
          message_buf[0] = sync_buf[0];
          message_buf[1] = sync_buf[1];

          // Once sync bytes are received, message id and length are known, use DMA to receive the rest of the message
          switch(message_id) {
            case IMURATEPVAS_ID:
              HAL_UART_Receive_DMA(&huart1, &message_buf[2], message_length + SHORT_HEADER_REMAINDER + CRC_LENGTH);
              sync_state = DMA_STATE;
              break;
            case BESTVEL_ID:
              // Workaround: use manually defined message length for BESTVEL
              HAL_UART_Receive_DMA(&huart1, &message_buf[2], BESTVEL_LENGTH + LONG_HEADER_REMAINDER + CRC_LENGTH);
              sync_state = DMA_STATE;
              break;
            default:
              sync_state = SYNC_BYTE_1_STATE;
              HAL_UART_Receive_IT(&huart1, sync_buf, 1);
              break;
          }
          return;
      }

      // Continue receiving. 2 bytes if getting message id next
      if (sync_state != MESSAGE_ID_STATE) {
        HAL_UART_Receive_IT(&huart1, sync_buf, 1);
      } else {
        HAL_UART_Receive_IT(&huart1, sync_buf, 2);
      }

    } else {
      BaseType_t xHigherPriorityTaskWoken = pdFALSE;
      
      // Send the buffer to the FreeRTOS queue
      xQueueSendFromISR(data_queue, message_buf, &xHigherPriorityTaskWoken);

      sync_state = SYNC_BYTE_1_STATE;
      message_length = 0;
      message_id = 0;

      // Restart interrupt-based reception for the next sync sequence
      HAL_UART_Receive_IT(&huart1, sync_buf, 1);

      // Perform context switch if necessary
      portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
    }
  }

  
}
